#!/usr/bin/perl -w
use strict;
#=========================================================================

#declaring an array and a variable to take input from user

#=========================================================================

my @numbers;

my $num;

do

{

#=========================================================================

#input number from user

#=========================================================================
print "please enter a number(+ve/-ve).";

$num=<>;

chomp($num);

#=========================================================================

#adding number at the back of array if number is +ve

#=========================================================================
if($num>0)

{

push (@numbers,$num);

}

#=========================================================================

#adding number at the front of array if number is -ve

#=========================================================================

elsif($num<0)

{

unshift (@numbers,$num);

}

}

while($num!=0);

#=========================================================================

#converting array to string and printing

#=========================================================================

$num=join(".",@numbers);

print "\nArray finalised: $num";

#=========================================================================

#initializing sum variable

#=========================================================================

my $sum=0;

#=========================================================================

#calculating sum of all elements in the array

#=========================================================================

foreach my $i(@numbers)

{

$sum+=$i;

}

print "\nSum of all elements in the array: $sum";

#============================END=============================================